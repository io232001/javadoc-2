package it.develhope.javadoc;
/**
 The ArithmeticOperators class provides methods for basic arithmetic operations.
 Autore: Ionut
 */
public class ArithmeticOperators
{
    /**
     The sum of two integer numbers.
     @param num1 an integer
     @param num2 an integer
     @return the sum of num1 and num2
     */
   public int sum(int num1,int num2)
   {
       return num1+num2;
   }
    /**
     The sub of two integer numbers.
     @param num1 an integer
     @param num2 an integer
     @return the sub of num1 and num2
     */
   public int sub(int num1,int num2)
   {
       return num1-num2;
   }
    /**
     The mul of two integer numbers.
     @param num1 an integer
     @param num2 an integer
     @return the Mul of num1 and num2
     */
    public int mul(int num1,int num2)
    {
        return num1*num2;
    }
    /**
     The div of two integer numbers.
     @param num1 an integer
     @param num2 an integer
     @return the div of num1 and num2
     */
    public int div(int num1,int num2)
    {
        return num1/num2;
    }
}
